#include "libmat.h"

t_mat4		mat4_subf(const t_mat4 a, const float b)
{
	t_mat4	ret;
	int		i;

	i = 0;
	while (i < 16)
	{
		ret.m[i] = a.m[i] - b;
		i++;
	}
	return (ret);
}
