#include "libmat.h"

t_mat4		mat4_scale(t_mat4 a, const t_vec3 b)
{
	t_mat4	ret;

	ret = mat4_set(1.0f, 1);
	ret.m[0] = b.x;
	ret.m[5] = b.y;
	ret.m[10] = b.z;
	ret = mat4_mul(ret, a);
	return (ret);
}
