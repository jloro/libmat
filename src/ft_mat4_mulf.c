#include "libmat.h"

t_mat4		ft_mat4_mulf(const t_mat4 a, const float b)
{
	t_mat4	ret;
	int		i;

	i = 0;
	while (i < 16)
	{
		ret.m[i] = a.m[i] * b;
		i++;
	}
	return (ret);
}
