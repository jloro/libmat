#include "libmat.h"

t_vec4		vec4_sub(const t_vec4 a, const t_vec4 b)
{
	t_vec4	ret;

	ret.x = a.x - b.x;
	ret.y = a.y - b.y;
	ret.z = a.z - b.z;
	ret.w = a.w - b.w;
	return (ret);
}
