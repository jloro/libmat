#include "libmat.h"

t_vec4		vec4_subf(const t_vec4 a, const float b)
{
	t_vec4	ret;

	ret.x = a.x - b;
	ret.y = a.y - b;
	ret.z = a.z - b;
	ret.w = a.w - b;
	return (ret);
}
