#include "libmat.h"

t_vec3		vec3_sub(const t_vec3 a, const t_vec3 b)
{
	t_vec3	ret;

	ret.x = a.x - b.x;
	ret.y = a.y - b.y;
	ret.z = a.z - b.z;
	return (ret);
}
