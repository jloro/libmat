#include "libmat.h"

t_vec3		vec3_normalize(t_vec3 a)
{
	float	norm;

	norm = vec3_norm(a);
	a.x = a.x / norm;
	a.y = a.y / norm;
	a.z = a.z / norm;
	return (a);
}
